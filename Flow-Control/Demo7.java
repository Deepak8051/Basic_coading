//Write a program to Print REVERSE of N to 1 numbers?
import java.util.Scanner;
public class Demo7 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("enter a number");
        int n = sc.nextInt();
        for(int i=n; i>=1; i--)
        System.out.println(i);
        
    }

    
}
